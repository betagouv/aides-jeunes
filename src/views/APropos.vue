    <h1>À propos de Mes Aides</h1>

    <h2 id="presentation">Mes Aides, c’est quoi ?</h2>

    <p>Simulez vos droits à plus d’une trentaine de prestations sociales en quelques minutes. Mes Aides compare votre situation personnelle aux critères des principales aides nationales, comme le RSA, la CMU-C ou les allocations familiales, ainsi que de certaines aides locales.</p>
    
    <p>Grâce aux informations que vous nous transmettez, nous estimons à quelles aides vous avez droit ainsi que leurs montants et conditions d'attribution. Vous pouvez ensuite engager les démarches auprès des organismes ciblés.</p>


    <h2 id="pourquoi">Pourquoi Mes Aides ?</h2>

    <p>Nous luttons contre le non-recours aux prestations sociales. Il s’agit de toutes les situations dans lesquelles une personne est en droit d’obtenir une aide mais ne la reçoit pas. De nombreuses causes sont invoquées comme le manque d’informations ou la complexité des démarches. Avec Mes Aides, nous proposons un résumé clair et synthétique.</p>

    <p>Mes Aides a été lancé en novembre 2014. Depuis janvier 2017, chaque jour, plus de 10 000 usagers évaluent leurs droits à vingt-quatre prestations sociales. Plus de la moitié reçoivent une estimation en moins de sept minutes.</p>

