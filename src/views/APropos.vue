    <h2 id="fonctionnement">Comment fonctionne Mes Aides ?</h2>

    <p>Mes Aides se base sur <a href="https://www.openfisca.fr">OpenFisca</a> : un moteur de calcul libre et ouvert utilisé par des chercheurs en économie et d’autres services publics. À partir des informations que vous entrez dans le simulateur, nous sommes capables d’évaluer votre situation sans avoir à interroger indépendamment chacun des organismes. Nous n’avons pas accès à vos dossiers ni ne transmettons vos informations : à vous de faire les démarches après la simulation.</p>

    <small>Mes Aides ne conserve aucune information nominative : en savoir plus sur <router-link to="/cgu">l'utilisation de vos données personnelles</router-link>.</small>


    <h2 id="mediateurs">Puis-je utiliser Mes Aides dans un contexte professionnel ?</h2>

